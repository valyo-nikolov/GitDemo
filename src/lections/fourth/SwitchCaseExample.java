package lections.fourth;

public class SwitchCaseExample {

    public static void main(String args[]) {
        int i = 2;
        switch (i) {
            case 1:
                System.out.println("Case1 ");
                break;
            case 2:
                System.out.println("Case2 ");
                break;
            case 3:
                System.out.println("Case3 ");
                break;
            default:
                System.out.println("Default ");
        }
    }

}
